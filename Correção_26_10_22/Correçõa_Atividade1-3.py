# receber dois numeros e fazer todas as operações matematicas
# incluindo resto da divisão e exponecial

print("programa para calcular operações matematicas")
num1 = float(input("digite o primeiro numero inteiro: "))
num2 = float(input("digite o segundo numero inteiro: "))
resultado = num1 + num2
print("a soma do numero 1 + numero 2 é: ", resultado)

resultado = num1 - num2
print("a subtraçõa do numero 1 - numero 2 é: ", resultado)

resultado = num1 * num2
print("a multiplicação do numero 1 * numero 2 é: ", resultado)

resultado = num1 / num2
print("a divisão do numero 1 / numero 2 é: ", resultado)

resultado = num1 % num2
print("o resto da divisão do numero 1 / numero 2 é: ", resultado)

resultado = num1 ** num2
print("o potencial do numero 1 pelo numero 2 é: ", resultado)

